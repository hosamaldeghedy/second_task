class ProductModel {
  final String name;
  final double price;
  final String description;
  final String image;
  final double rating;
  final int reviewsCount;
  final String type;
  final String material;

  ProductModel({
    required this.name,
    required this.price,
    required this.description,
    required this.image,
    required this.rating,
    required this.reviewsCount,
    required this.type,
    required this.material,
  });
}
